#include "../bin.h"

int	bin_ft_ismetachar(int c)
{
	return (c == '$' || c == ' ' || c == '|' || c == '<' \
	|| c == '>' || c == '/' || c == '\'' || c == '\"');
}

int	bin_ft_isseparator(int c)
{
	return (c == ' ' || c == '|' || c == '<' || c == '>');
}

int	bin_ft_isquote(int c)
{
	return ((c == 39) || (c == 34));
}
