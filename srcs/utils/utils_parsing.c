#include "minishell.h"

int	ft_ismetachar(int c)
{
	return (c == '$' || c == ' ' || c == '|' || c == '<' \
	|| c == '>' || c == '/' || c == '\'' || c == '\"');
}

int	ft_isseparator(int c)
{
	return (c == ' ' || c == '|' || c == '<' || c == '>');
}

int	ft_isquote(int c)
{
	return ((c == 39) || (c == 34));
}
